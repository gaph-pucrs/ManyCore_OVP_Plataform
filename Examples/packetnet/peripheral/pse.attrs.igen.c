static ppmBusPort busPorts[] = {
    {
        .name            = "bport1",
        .type            = PPM_SLAVE_PORT,
        .addrHi          = 0x0LL,
        .mustBeConnected = 1,
        .remappable      = 0,
        .description     = 0,
    },
    { 0 }
};

static PPM_BUS_PORT_FN(nextBusPort) {
    if(!busPort) {
        busPort = busPorts;
    } else {
        busPort++;
    }
    return busPort->name ? busPort : 0;
}


//                     Shared data for packetnet interface

Uns8 pktPort_pnsd[8];

static ppmPacketnetPort packetnetPorts[] = {
    {
        .name            = "pktPort",
        .mustBeConnected = 0,
        .description     = 0,
        .sharedData      = pktPort_pnsd,
        .sharedDataBytes = 8,
        .handlePtr       = &handles.pktPort,
        .packetnetCB     = pktTrigger,
        .userData        = (void*)33
    },
    { 0 }
};

static PPM_PACKETNET_PORT_FN(nextPacketnetPort) {
    if(!port) {
         port = packetnetPorts;
    } else {
        port++;
    }
    return port->name ? port : 0;
}

